    row_len = len(grid)
    col_len = len(grid[0])

    # 2
    d = {}
    for r in range(row_len):
        for c in range(col_len):
            one = grid[r][c]
            if one == 0:
                d["%s_%s" % (r, c)] = {"max": {"land_row": float("-inf"), "land_col": float("-inf"), "distance": float("-inf")},
                                       "min": {"land_row": float("inf"), "land_col": float("inf"), "distance": float("inf")}
                                       }

    # 3
    for r in range(row_len):
        for c in range(col_len):
            one = grid[r][c]
            if one == 1:
                # 将当前陆地 和 d 中的所有海洋计算, 并动态更新 d 和 max_ocean
                for ocean in d:
                    mid = ocean.index("_")
                    ocean_row = int(ocean[:mid])
                    ocean_col = int(ocean[mid + 1:])

                    curr_distance = abs(r - ocean_row) + abs(c - ocean_col)
                    # if curr_distance > d[ocean]["max"]["distance"]:
                    #     d[ocean]["max"]["distance"] = curr_distance
                    #     d[ocean]["max"]["land_row"] = r
                    #     d[ocean]["max"]["land_col"] = c

                    if curr_distance < d[ocean]["min"]["distance"]:
                        # print("陆地[%s, %s]到[%s]海洋的距离=%s, 比d=%s要小" % (r, c, ocean, curr_distance, d[ocean]["min"]["distance"]))
                        d[ocean]["min"]["distance"] = curr_distance
                        d[ocean]["min"]["land_row"] = r
                        d[ocean]["min"]["land_col"] = c
                        # print("更新后d[%s]=%s" % (ocean, d[ocean]["min"]["distance"]))

                    # if d[ocean]["min"]["distance"] > max_ocean["distance"]:
                    #     max_ocean["row"] = ocean_row
                    #     max_ocean["col"] = ocean_col
                    #     max_ocean["distance"] = d[ocean]["min"]["distance"]
                        # print("d[%s][min]=%s" % (ocean, d[ocean]["min"]))
                        # print("max_ocean=%s" % max_ocean)
                    # print("")
    # 4
    if len(d) == 0:
        return -1
    tmp = max(list(d.values()), key=lambda x: x["min"]["distance"])
    return int(tmp["min"]["distance"]) if tmp["min"]["distance"] != float("inf") else -1


